from collections import Counter

import jieba
import matplotlib.pyplot as plt

# 設定 matplotlib 中文字體
plt.rcParams['font.sans-serif'] = ['Microsoft JhengHei', 'SimHei', 'Arial Unicode MS']  # 繁體中文字型
plt.rcParams['axes.unicode_minus'] = False  # 解決負號顯示問題

# 假設這是你的文本
text = """根據歷年營養調查，國人每日平均鈉攝取量超過建議量上限2400毫克的1.5倍以上，對心血管、腎臟和血壓控制都帶來長期的健康風險。每餐建議鈉攝取應控制在800毫克以內，惟在台灣，大小餐廳林立，外食已成多數人日常飲食與各種聚會的主要型態，控制鈉攝取量是一大挑戰！近期一項針對台灣常見中式外食的研究發現，許多餐點的鈉含量高，尤其國人喜歡吃的湯麵、乾麵類，像是排骨麵、紅燒牛肉麵、炸醬麵這類使用醃料再油炸或滷製食材的餐點，更是高鈉代表。另外，炒飯類，因為炒製時醬汁滲入米粒，也會攝取較多鈉鹽。上述研究探討如何經由進食技巧以減少鈉鹽攝取量，即使是外食族，也可以有效減少鈉的攝取。最明顯的例子是湯麵，只要少喝湯，每100公克就能減去約400毫克的鈉。乾麵、燴飯及炒菜類餐點則可利用「過水」的方式，在上桌後以開水涮洗、瀝去多餘的醬汁再吃，每100公克大約可減少100至140毫克的鈉。而台灣常見的小吃，如肉圓、蚵仔煎等，有些本體含鈉不高，但額外添加的淋醬與沾醬往往才是鈉含量暴增的主因。研究顯示，避免這些醬料，每100公克餐點可少吃下120至210毫克的鈉。不過，肉類或滷味小菜類因醃製時間長、調味深入食材內，即使過水處理，效果仍然有限。其它像燴飯類雖然鈉含量高，但只要燴料湯汁不全加，就能明顯降低鈉攝取量；例如沙茶牛肉燴飯就比牛肉炒飯有更佳的減鈉效果。蔬菜類原本應為低鈉選項，但若加入蠔油、豆瓣醬等高鈉調味，也可能讓一盤看似健康的青菜，變成高鈉地雷。對於經常外食者，需要學習一些減鈉技巧，在沒有改變點餐習慣下，只要稍微調整食用方式，就能輕鬆達成減鈉的成效。

面對全球普遍過量的鈉攝取問題，許多國家早已積極推動減鈉政策。包括產品配方調整、增加包裝食品正面彩色營養標示、課徵高鈉稅等措施，這顯示出，單靠知識性教育遠遠不夠，實際行動與制度面的介入才是改變關鍵。成功的案例包括西方人日常食用的土司麵包的減鈉配方改良；100公克吐司約含400毫克的鈉，一般人並不知道吐司還沒加任何佐料已經含可觀的鈉量。由於現代社會加工食品愈來愈普及，例如泡麵、冷凍食品、餅乾零食等，透過增加包裝食品的正面標示，設計成紅黃綠色或是標示健康等級，提醒消費者選擇低鈉低糖的食品，這些都能創造健康飲食的支持性環境。每年五月十七日是世界高血壓日，而這一週也是世界減鹽行動組織 (WASH, World Action on Salt & Health；已改為WASSH，增加減糖Sugar任務) 的宣導週，期盼產官學協力積極展開減鹽行動，促進國人健康。"""

# 2. 分詞 (中文)
words = jieba.cut(text)

# 3. 去除停用詞 (簡化版，實際應有完整停用詞表)
stopwords = {'是', '一種', '廣泛', '使用'}
filtered_words = [word for word in words if word not in stopwords and len(word) > 1] # 過濾掉停用詞和長度小於等於1的詞彙

# 4. 統計詞頻
word_counts = Counter(filtered_words) # 使用  Counter  統計各詞彙出現的次數
print(f"詞頻統計: {word_counts}")

# 5. 顯示前 10 個高頻詞
top_n = 10
most_common_words = word_counts.most_common(top_n)
print(f"前 {top_n} 高頻詞: {most_common_words}")
